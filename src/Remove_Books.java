    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost/library";
        String user = "root";
        String pwd = "Ryaa";
        String input = t1.getText();
        String query = "delete from books where BOOK_ID = '"+input+"' or BOOK_NAME = '"+input+"';";
        
        try{
            Connection conn = DriverManager.getConnection(url,user,pwd);
            Statement stm = conn.createStatement();
            int row = stm.executeUpdate(query);
            if(row>0)
            {
                JOptionPane.showMessageDialog(this, "Book Removed from Library");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "No Books found.....!!!");
            }
            
            stm.close();
            t1.setText(null);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

